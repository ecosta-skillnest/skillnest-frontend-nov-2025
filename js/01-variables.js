// Las variables en JavaScript se pueden declarar usando var, let o const.

// Declaración con var
var nombre = "Ada";
console.log(nombre); // Salida: Ada

// Declaración con let
let edad = 30;
console.log(edad); // Salida: 30

// Declaración con const (constante, no se puede reasignar)
const PI = 3.14;
console.log(PI); // Salida: 3.14

// Tipos de datos comunes en JavaScript
let numero = 42; // Número
let texto = "Hola mundo"; // Cadena de texto
let booleano = true; // Booleano

// Otros tipos de datos
let arreglo = [1, 2, 3]; // Arreglo (o lista)
let objeto = { nombre: "Ana", edad: 25 }; // Objeto (o diccionario)
let nulo = null; // Null significa ausencia de valor
let indefinido; // Undefined significa que la variable no ha sido asignada

console.log(numero, texto, booleano, arreglo, objeto, nulo, indefinido);